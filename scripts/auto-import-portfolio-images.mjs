#!/usr/bin/env node
/**
 * Inkspirations Studios Portfolio Image Auto-Importer
 *
 * Run from repo root:
 *   node scripts/auto-import-portfolio-images.mjs --max=60
 *
 * This script scans the repository for image files, scores likely portfolio
 * candidates, copies selected files into assets/images/portfolio/, and rebuilds
 * data/portfolio-gallery.json. Robert should not have to place images manually.
 */

import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';

const root = process.cwd();
const args = new Map(process.argv.slice(2).map(arg => {
  const [k, v] = arg.replace(/^--/, '').split('=');
  return [k, v ?? true];
}));

const dryRun = args.has('dry-run');
const maxItems = Number(args.get('max') || 40);
const outputDir = path.join(root, 'assets', 'images', 'portfolio');
const outputJson = path.join(root, 'data', 'portfolio-gallery.json');
const imageExts = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif', '.avif']);
const skipDirs = new Set(['.git', 'node_modules', '.next', 'dist', 'build']);

function walk(dir, results = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (skipDirs.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, results);
    else if (imageExts.has(path.extname(entry.name).toLowerCase())) results.push(full);
  }
  return results;
}

function scoreImage(file) {
  const rel = path.relative(root, file).replaceAll(path.sep, '/').toLowerCase();
  let score = 0;
  if (rel.includes('portfolio')) score += 25;
  if (rel.includes('artwork') || rel.includes('art')) score += 20;
  if (rel.includes('fluid') || rel.includes('soul') || rel.includes('ink') || rel.includes('ocean') || rel.includes('blue')) score += 18;
  if (rel.includes('coaster') || rel.includes('wood') || rel.includes('functional')) score += 10;
  if (rel.includes('thumb')) score -= 4;
  if (rel.includes('icon') || rel.includes('logo') || rel.includes('favicon')) score -= 25;
  return score;
}

function titleFromFile(file) {
  return path.basename(file, path.extname(file)).replace(/[-_]+/g, ' ').replace(/\b\w/g, letter => letter.toUpperCase());
}

function stableId(file) {
  const base = path.basename(file, path.extname(file)).toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
  const hash = crypto.createHash('sha1').update(file).digest('hex').slice(0, 6);
  return `${base}-${hash}`;
}

function inferTags(name) {
  const lower = name.toLowerCase();
  const tags = ['portfolio'];
  ['blue', 'wood', 'coaster', 'ink', 'ocean', 'wave', 'fluid', 'functional', 'cover', 'artwork'].forEach(tag => {
    if (lower.includes(tag)) tags.push(tag);
  });
  return [...new Set(tags)];
}

function inferType(name) {
  const lower = name.toLowerCase();
  if (lower.includes('coaster')) return 'Coaster / Object Study';
  if (lower.includes('wood')) return 'Wood / Functional Art';
  if (lower.includes('cover') || lower.includes('fluid') || lower.includes('soul')) return 'Finished Artwork';
  return 'Portfolio Image';
}

fs.mkdirSync(outputDir, { recursive: true });
fs.mkdirSync(path.dirname(outputJson), { recursive: true });

const candidates = walk(root)
  .filter(file => !path.relative(root, file).replaceAll(path.sep, '/').startsWith('assets/images/portfolio/'))
  .map(file => ({ file, score: scoreImage(file) }))
  .filter(item => item.score > 0)
  .sort((a, b) => b.score - a.score)
  .slice(0, maxItems);

const items = [];
for (const [index, candidate] of candidates.entries()) {
  const originalName = path.basename(candidate.file);
  const destination = path.join(outputDir, originalName);
  const relDestination = path.relative(root, destination).replaceAll(path.sep, '/');
  if (!dryRun) fs.copyFileSync(candidate.file, destination);
  items.push({
    id: stableId(candidate.file),
    title: titleFromFile(originalName),
    collection: index === 0 ? 'Signature Portfolio' : 'Repository Portfolio Import',
    type: inferType(originalName),
    year: '2026',
    medium: 'Repository image / portfolio candidate',
    image: relDestination,
    alt: `${titleFromFile(originalName)} portfolio image.`,
    description: 'Auto-imported portfolio candidate selected from the repository image files for Robert review.',
    featured: index === 0,
    priority: index + 1,
    tags: inferTags(originalName)
  });
}

const output = {
  galleryTitle: 'Inkspirations Studios Interactive Portfolio Gallery',
  schemaVersion: '1.0.0',
  updated: new Date().toISOString().slice(0, 10),
  note: 'Generated by scripts/auto-import-portfolio-images.mjs. Review before treating all entries as final portfolio selections.',
  items
};

if (!dryRun) fs.writeFileSync(outputJson, JSON.stringify(output, null, 2) + '\n');
console.log(`${dryRun ? 'Dry run found' : 'Imported'} ${items.length} portfolio image candidate(s).`);
